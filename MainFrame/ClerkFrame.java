    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
    if(jTextField3.getText().equals("")||jLabel25.getText().equals("--")){
        JOptionPane.showMessageDialog(this, "Please provide account no before proceed ");
    }else{
        if(!(jTextField3.getText().equals(""))){
            try{
                if(Integer.parseInt(jTextField4.getText())<=0||jTextField4.getText().substring(0, 1).equals("+")){
                JOptionPane.showMessageDialog(this, "Invalid amount");
            }
                else if(!(Integer.parseInt(jTextField4.getText())>Integer.parseInt(jLabel25.getText()))){
                
            
            if(Integer.parseInt(jTextField4.getText())>50000){
            JOptionPane.showMessageDialog(this, "Amount more than 50,000 can't be Credit");
        }else{
                updateAccount("Debit");
                JOptionPane.showMessageDialog(this, "Amount Successfully Debited");
                resetPanel3();
            }
            }else{
                JOptionPane.showMessageDialog(this, "Insufficient Fund");
            }
        }catch(HeadlessException | NumberFormatException e){
            JOptionPane.showMessageDialog(this, "You have entered invalid amount value");
        }
        
        }else{
            JOptionPane.showMessageDialog(this, "Please Enter the amount before proceed");
        }
    }
    }//GEN-LAST:event_jButton11ActionPerformed

