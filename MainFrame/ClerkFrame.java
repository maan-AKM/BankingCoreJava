    private String genrateTransactionID() {
        String trans_id = "10152017";
        try {
            rs = con.prepareStatement("Select count(*) from Transaction_Details ").executeQuery();rs.next();
            if(Integer.parseInt(rs.getString(1))!=0){
                rs = con.prepareStatement("Select max(Transaction_No) from Transaction_Details").executeQuery();rs.next();
                trans_id = (Long.parseLong(rs.getString(1))+1)+"";
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    return trans_id;
    }

